// Obtener frase aleatoria de un genero especifico
export const getFraseAleatoria = (generoId) => {
  const frasesGenero = frases[generoId] || frases.terror
  const indice = Math.floor(Math.random() * frasesGenero.length)
  return frasesGenero[indice]
}

// Obtener todas las frases de un genero
export const getFrasesGenero = (generoId) => frases[generoId] || frases.terror

// Obtener frase especifica por genero e indice
export const getFrase = (generoId, indice) => {
  const frasesGenero = frases[generoId] || frases.terror
  return frasesGenero[indice % frasesGenero.length]
}

